            <strong>
                La seule manière pour un deck wall de perdre de le Game Win est de laisser sa proie gagner des Victory Points ou de laisser la table aller au temps.
            </strong>
            Relisez cette phrase, je sais qu'elle va en surprendre plus d'une personne.
        </p>
        <p>
            Un deck wall a un contrôle total sur le Game Win. Les seules façons dont il peut échouer pour remporter la victoire sont :
        </p>
        <ul>
            <li>
                Permettre à sa proie d'obtenir 3 Victory Points, éliminant ainsi toute autre personne de la compétition pour le Game Win ;
            </li>
            <li>
                Permettre à sa proie d'obtenir 2 Victory Points et à une autre personne d'obtenir 1 Victory Point, garantissant que le mieux que le deck wall puisse faire est d'égaliser ou de permettre à un autre joueur d'obtenir un Game Win ;
            </li>
            <li>
                Permettre à sa proie d'obtenir 1 Victory Point et à une autre personne d'obtenir 2 Victory Points ; cela revient à la situation ci-dessus ;
            </li>
            <li>
                Laissez la table aller au temps, garantissant que personne n'obtiendra un Game Win.
            </li>
        </ul>
        <p>
            Aucune de ces options n'est acceptable lorsque vous jouez avec un  deck wall. Par conséquent, il devrait y avoir deux objectifs principaux :
        </p>
        <ol type="1">
            <li>
                Empêcher sa proie de ne gagner aucun Victory Point par quelque moyen que ce soit ;
            </li>
            <li>
                S'assurer que la table n'aille au temps en aucune circonstance.
            </li>
        </ol>

        <p>
            Non. Tu ne l’es pas. Les boss gagnent des parties.
        </p>
        <p>
            Tu vas faire un trou, ensuite quelqu’un va gagner la table. Tu es en train d’essayer de jouer rush combat avec un setup wall ; on va appeler ça un “combat moins stupide” (allez, ragez les trolls :D)
        </p>
        <p>
            Les murs arrêtent des joueurs, pas des sbires. Les decks wall sont plus proches des decks grinder que des decks combat. Comme en bio, tu peux étudier leurs ADN pour trouver des ancêtres communs (et encore un autre article).
        </p>
        <p>
            Avant tout, TU DOIS SURVIVRE AU COMBAT.
        </p>
        <p>
            Oui, la master out-of-turn du dessus t’aide à survivre UN combat entre tes tours. Mais pour les autres ?
        </p>
        <p>
            Si tu as plus d’un “bon combat”, tu finiras avec plus de sang sur tes vampires, plus de facilité pour sortir un joueur (pour peut-être gagner la table), plus de points de vie (pour être moins sous pression de bloquer le bleed à 2 d’<span class="krcg-card">Enkidu, the Noah</span>, ou pour avoir plus de vampires réveillés) et avoir plus d'équipements permanents est plus important qu'UN combat soit bon.
        </p>
        <p>
            Certes, sur le face à face, la carte est bonne, mais c’est alors une carte "win-more".
        </p>
        <p>
            Les actions lors de la Master Phase sont importantes. Par exemple, <span class="krcg-card">The Rack</span> fonctionne peu importe si tu puisses bloquer ET aggraver en même temps.
        </p>
        <p>
            Ton Lambach meurt de la même manière qu’<span class="krcg-card">Isabel Giovanni</span> : quand ils n’ont plus de sang sur eux.
        </p>
        <p>
            <span class="krcg-card">Guardian Angel</span> est meilleur que Rötschreck.
        </p>

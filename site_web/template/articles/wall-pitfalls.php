        <p>
            Depuis un certain temps, je suis intrigué par les Wall Tzimisce, parce que je vois constamment des gens jouer des deck non optimaux, de mon point de vue. J’ai aussi vu des gens utiliser des idées qui leur étaient propres et que je pensais pouvoir intégrer au concept général. Deux exemples sont <span class="krcg-card">Banshee Ironwail</span> (un must dans ce deck à mon avis) et <span class="krcg-card">Sascha Vykos, The Angel of Caine ADV</span> (le meilleur Tzimisce), comme me l’ont démontré Nuno Moure et Carlos Santos, respectivement. C’est étonnant comme les gens préfèrent <span class="krcg-card">Lambach</span> à Vykos ADV. J’ai dû apprendre à jouer avec un deck Wall, car je n’ai jamais vraiment aimé cet archétype.
        </p>
        <p>
            Bien sûr, mon Grinder ou mon Nephandus deviennent des murs fréquemment, mais jouer avec un deck qui ne fait que bloquer et qui ne peut pas infliger 8 ou 10 dégâts dans les points de vie en un tour est quelque chose que je n’ai jamais aimé. Le premier choix évident était de jouer plus de <span class="krcg-card">Smiling Jack, the Anarch</span>. Ensuite il a fallu gérer la pression constante des vampires qui ont +1 bleed. En plus de l'exercice de construction et de jeu, je me sentais tellement frustré par des joueurs inexpérimentés et toujours arrogants comme des stars de la lutte ou des enfants gâtés, que j'ai écrit le texte ci-dessous comme une diatribe.
        </p>
        <p>
            J’avais prévu ce texte pour le publier quand, et seulement quand, je gagnerais un tournois avec le deck que j’ai construit en rédigeant cet article. J’ai pensé que cela me prendrait des mois ou des années. J’ai réussi à mon premier essai.
        </p>

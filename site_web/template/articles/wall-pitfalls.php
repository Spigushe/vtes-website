            Les murs ne sont pas des balais.
        </p>
        <p>
            D’après le système de classification de Marin-Ribas (encore un autre article, je sais), les decks wall tendent vers la catégorie des jeux à 3 joueurs.
        </p>
        <p>
            Les decks avec <span class="krcg-card">Eagle’s Sight</span> tendent vers l’objectif d’empêcher n’importe qui d’être sorti. Cela crée de la tension en jeu, du conflit entre les joueurs et de très grosses erreurs de jeu. Et les tables finissent sans gagnant.
        </p>
        <p>
            Charlie Sheen n’aime pas ça.
        </p>
        <p>
            Un bon deck wall devrait gagner 80% à 90% des parties à 3 joueurs dans lesquelles il se retrouve, SI il utilise le temps jusqu’à ce moment à se préparer.
        </p>
        <p>
            Bien sûr, les joueurs intelligents vont le voir venir de loin, mais … Smiling Jack avec des glaçons est une super boisson qui permet aux jeux bleed de faire le travail pour vous.
